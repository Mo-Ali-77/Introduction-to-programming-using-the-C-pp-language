#include<iostream>

using namespace std;

int main()
{
    cout << "M1\\M2" << endl; // Print Backslash.
    
    cout << "M3\tM4\n"; // Tab (4 spaces).
    
    cout << "M5\nM6" << endl; // New line.
    
    cout << "M5\"M6" << endl; // Double qoutation.
    
    cout << "M5\'M6" << endl; // Single qoutation.
    
    cout << "M5\aM6" << endl; // Audio bell.
    
    cout << "my name \"mohammed\""; // Example.

	return 0;
}